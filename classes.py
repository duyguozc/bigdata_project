class Tour:
  def __init__(self, id, label, st_date, end_date, total_seats, avail_seats, dest, fare, desc):
    self.id = id
    self.label = label
    self.st_date = st_date
    self.end_date = end_date
    self.total_seats = total_seats
    self.avail_seats = avail_seats
    self.dest = dest
    self.fare = fare
    self.desc = desc